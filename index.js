// Define a helper function to calculate the area of a rectangle
function calculateRectangleArea(width, height) {
    return width * height;
}

// Define width and height values for the rectangle
var width = 5;
var height = 10;

// Calculate the area of the rectangle using the helper function
var area = calculateRectangleArea(width, height);

// Output the calculated area
console.log("The area of the rectangle with width", width, "and height", height, "is:", area);
